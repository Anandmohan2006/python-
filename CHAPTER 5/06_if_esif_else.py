a = int(input("Enter your age: "))

if a >= 60:
    print("You can now retire.")

elif a >= 26:
    print("You can now go to work.")

elif a >= 22:
    print("You can now go to university.")

elif a >= 18:
    print("You can now go to college.")

elif a >= 14:
    print("You can now go to high school.")

elif a >= 10:
    print("You can now go to middle school.")

elif a >= 5:
    print("You can now go to primary school.")

elif a >= 3:
    print("You can now go to school.")

else:
    print("Too young for school.")